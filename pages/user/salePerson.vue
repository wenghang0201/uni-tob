			};
		},
		filters: {
			//格式化日期
			formatDate(data, that) {
				return that.globalUtil.dateTimeFormat(data)
			},
		},
		onLoad(e) {
			if(!e.uid){
				uni.showToast({
					title:"请选择用户",
					icon:'none'
				})
				return false;
			}
			this.queryParam.userId = e.uid;
			this.init()
		},
		methods: {
			detail(item){
				uni.navigateTo({
					url: "/pages/user/info?uid=" + item.id
				});
			},
			init(){
				getAllSale(this.queryParam).then(res => {
					if(res.success){
						this.salePersons = res.voList
					}
				})
			},
			
		}
	}
</script>

