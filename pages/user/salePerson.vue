<script> 
	import {
		getAllSale
	} from '@/api/user.js'
	export default {
		data() {
			return {
				radio: '',
				form:"",
				updateShow:false,
				rechargeId:0,
				rechargeMoney:0,
				that: this,
				salePersons:[],
				//查询条件
				queryParam: {
					pageNo: 1,
					pageSize: 10,
					nickname:"",
					userId:0
				},
				radioList: [
					{
						value:'0',
						name: '加款'
					},
					{
